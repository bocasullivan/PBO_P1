package Lat_Constructor2;

public class kotak {
    private int panjang;
    private int lebar;

    public kotak(){
    }

    public kotak(int panjang, int lebar){
        this.panjang = panjang;
        this.lebar = lebar;
    }
    public int getPanjang(){
        return panjang;
    }
    public int getLebar(){
        return lebar;
    }
}
